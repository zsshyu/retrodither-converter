export const BAYER_32X32 = generateBayerMatrix(32);

// Generate Bayer matrix recursively
function generateBayerMatrix(n: number): number[][] {
  if (n === 2) {
    return [[0, 2], [3, 1]];
  }

  const half = n / 2;
  const prev = generateBayerMatrix(half);
  const matrix: number[][] = Array(n).fill(0).map(() => Array(n).fill(0));

  for (let i = 0; i < half; i++) {
    for (let j = 0; j < half; j++) {
      const val = prev[i][j];
      matrix[i][j] = 4 * val;
      matrix[i][j + half] = 4 * val + 2;
      matrix[i + half][j] = 4 * val + 3;
      matrix[i + half][j + half] = 4 * val + 1;
    }
  }

  return matrix;
}

export function getBayerMatrix(size: 2 | 4 | 8 | 16 | 32): number[][] {
  switch (size) {
    case 2: return BAYER_2X2;
    case 4: return BAYER_4X4;
    case 8: return BAYER_8X8;
    case 16: return BAYER_16X16;
    case 32: return BAYER_32X32;
  }
}
